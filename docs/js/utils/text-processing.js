/**
 * Text Processing Utilities
 *
 * Functions for processing and transforming text for TTS and display.
 */

// Escapes <, >, &, ", '. textContent-based escaping covers <, >, & but leaves quotes —
// safe between tags but not in attribute contexts. We escape quotes too so callers can use
// this for both `<span>${x}</span>` and `<div data-x="${x}">` without thinking about it.
const HTML_ESCAPE_MAP = { '&': '&amp;', '<': '&lt;', '>': '&gt;', '"': '&quot;', "'": '&#39;' };
export function escapeHtml(text) {
  if (text == null) return '';
  return String(text).replace(/[&<>"']/g, c => HTML_ESCAPE_MAP[c]);
}

export function escapeRegExp(text) {
  return String(text).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

// Sanitize HTML restored from a save file. Saves were generated by GlkOte but a malicious
// save (file import or shared Drive folder) could contain script/event-handler payloads.
// <template> content is inert — scripts don't execute and resources don't load while we walk it.
const DANGEROUS_TAGS = new Set(['script', 'iframe', 'object', 'embed', 'style', 'link', 'meta', 'base', 'form']);
const URL_ATTRS = new Set(['href', 'src', 'xlink:href', 'action', 'formaction']);
const URL_SCHEME_BLOCKLIST = /^\s*(javascript|data|vbscript):/i;

export function sanitizeRestoredHTML(html) {
  if (typeof html !== 'string' || html === '') return '';
  const template = document.createElement('template');
  template.innerHTML = html;
  const walker = document.createTreeWalker(template.content, NodeFilter.SHOW_ELEMENT);
  const toRemove = [];
  let node;
  while ((node = walker.nextNode())) {
    if (DANGEROUS_TAGS.has(node.tagName.toLowerCase())) {
      toRemove.push(node);
      continue;
    }
    for (const attr of [...node.attributes]) {
      const name = attr.name.toLowerCase();
      if (name.startsWith('on')) {
        node.removeAttribute(attr.name);
      } else if (URL_ATTRS.has(name) && URL_SCHEME_BLOCKLIST.test(attr.value)) {
        node.removeAttribute(attr.name);
      }
    }
  }
  toRemove.forEach(n => n.remove());
  return template.innerHTML;
}

/**
 * Process text for TTS - normalize formatting and apply transformations
 * @param {string} text - Text to process
 * @returns {string} Processed text
 */
export function processTextForTTS(text) {
  let processed = text
    // Collapse spaced capitals: "A N C H O R H E A D" → "ANCHORHEAD"
    .replace(/\b([A-Z])\s+(?=[A-Z](?:\s+[A-Z]|\s*\b))/g, '$1')
    // Normalize initials: "H.P." → "H P"
    .replace(/\b([A-Z])\.\s*/g, '$1 ')
    .replace(/\b([A-Z])\s+([A-Z])\s+/g, '$1$2 ')
    // Collapse whitespace
    .replace(/\s+/g, ' ')
    .trim();

  // Title case for all-caps words (4+ letters): "ANCHORHEAD" → "Anchorhead"
  processed = processed.replace(/\b([A-Z]{4,})\b/g, (match) => {
    return match.charAt(0) + match.slice(1).toLowerCase();
  });

  return processed;
}

/**
 * Split processed text into sentences at delimiters
 * Used by both createNarrationChunks() and insertSentenceMarkersInHTML()
 * @param {string} processedText - Text to split
 * @returns {string[]} Array of sentence chunks
 */
export function splitIntoSentences(processedText) {
  if (!processedText) return [];

  // Split after markers OR after punctuation (when no marker present)
  // Pattern 1: Split after marker (with or without space) → keeps marker in chunk
  // Pattern 2: Split after punctuation+space when NOT followed by marker
  const chunks = processedText
    .split(/(?<=⚐\d+⚐)\s*|(?<=[.!?])(?!⚐)\s+/)
    .map(chunk => chunk.trim())
    .filter(chunk => chunk.length > 0);

  // If no chunks found, use whole text
  return chunks.length > 0 ? chunks : [processedText];
}

/**
 * Process text for TTS and split into sentences (combined operation)
 * @param {string} text - Raw text to process
 * @returns {string[]} Array of processed sentence chunks
 */
export function processAndSplitText(text) {
  if (!text) return [];

  const processed = processTextForTTS(text);

  // Split into sentences
  // Pattern 1: Split after marker (with or without space) → keeps marker in chunk
  // Pattern 2: Split after punctuation+space when NOT followed by marker
  const chunks = processed
    .split(/(?<=⚐\d+⚐)\s*|(?<=[.!?])(?!⚐)\s+/)
    .map(chunk => chunk.trim())
    .filter(chunk => chunk.length > 0);

  // Filter out metadata chunks individually (not the entire text block)
  const metadataPatterns = [
    /Release\s+\d+/i,
    /Serial\s+number\s+\d+/i,
    /Inform\s+v[\d.]+/i,
    /Library\s+[\d/]+/i,
    /Interpreter\s+[\d.]+/i
  ];

  const filteredChunks = chunks.filter(chunk => {
    // Remove marker for metadata check
    const cleanChunk = chunk.replace(/⚐\d+⚐/g, '').trim();
    return !metadataPatterns.some(pattern => pattern.test(cleanChunk));
  });

  // If no chunks left after filtering, return original chunks
  return filteredChunks.length > 0 ? filteredChunks : chunks;
}

/**
 * Calculate Levenshtein distance between two strings
 * @param {string} str1 - First string
 * @param {string} str2 - Second string
 * @returns {number} Edit distance
 */
export function levenshteinDistance(str1, str2) {
  const m = str1.length;
  const n = str2.length;
  const dp = Array(m + 1).fill(null).map(() => Array(n + 1).fill(0));

  for (let i = 0; i <= m; i++) dp[i][0] = i;
  for (let j = 0; j <= n; j++) dp[0][j] = j;

  for (let i = 1; i <= m; i++) {
    for (let j = 1; j <= n; j++) {
      if (str1[i - 1] === str2[j - 1]) {
        dp[i][j] = dp[i - 1][j - 1];
      } else {
        dp[i][j] = 1 + Math.min(
          dp[i - 1][j],    // Delete
          dp[i][j - 1],    // Insert
          dp[i - 1][j - 1] // Replace
        );
      }
    }
  }

  return dp[m][n];
}

/**
 * Calculate similarity ratio between two strings (0 = different, 1 = identical)
 * @param {string} str1 - First string
 * @param {string} str2 - Second string
 * @returns {number} Similarity ratio 0-1
 */
export function textSimilarity(str1, str2) {
  if (!str1 || !str2) return 0;
  if (str1 === str2) return 1;
  const maxLen = Math.max(str1.length, str2.length);
  if (maxLen === 0) return 1;
  return 1 - (levenshteinDistance(str1, str2) / maxLen);
}
